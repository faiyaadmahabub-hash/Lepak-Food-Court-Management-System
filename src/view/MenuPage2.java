    private void btPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPayActionPerformed
        double totalAmount = Double.parseDouble(totalAmountField.getText());

        Customer currentCustomer = (Customer) UserController.getLoggedInUser();
        if (currentCustomer == null) {
            JOptionPane.showMessageDialog(this, "Error: No logged-in customer!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (currentCustomer.deductBalance(totalAmount)) {
            JOptionPane.showMessageDialog(this, "Payment Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);

            // ✅ Save updated customer data
            FileHandler.saveUsers();
        } else {
            JOptionPane.showMessageDialog(this, "Insufficient balance!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_btPayActionPerformed

