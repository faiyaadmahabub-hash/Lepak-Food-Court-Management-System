    private void updateOrderSummary() {
        double total = orderList.stream().mapToDouble(item -> item.getMenuItem().getPrice() * item.getQuantity()).sum();
        txtTotal.setText(String.format("%.2f", total));
    }

    private void deleteItem() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            orderList.remove(selectedRow);
            updateOrderSummary();
        } else {
            JOptionPane.showMessageDialog(this, "Please select an item to delete.");
        }
    }

    private void placeOrder() {
        if (!orderList.isEmpty()) {
            OrderController.createOrder(orderList);
            JOptionPane.showMessageDialog(this, "Order placed successfully!");
            orderList.clear();
            updateOrderSummary();
        } else {
            JOptionPane.showMessageDialog(this, "Cart is empty.");
        }
    }

private void updateTotalPrice() {
    DefaultTableModel model = (DefaultTableModel) orderTable.getModel();
    double totalPrice = 0.0;

    for (int i = 0; i < model.getRowCount(); i++) {
        totalPrice += (double) model.getValueAt(i, 4);
    }

    totalAmountField.setText(String.format("%.2f", totalPrice)); // ✅ Display total
}

    private void resetOrder() {
        orderList.clear();
        updateOrderSummary();
    }

