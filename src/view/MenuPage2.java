    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        int selectedRow = menuTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an item to order.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String itemId = (String) menuTable.getValueAt(selectedRow, 0);
        String itemName = (String) menuTable.getValueAt(selectedRow, 1);
        double price = (double) menuTable.getValueAt(selectedRow, 2);

        int quantity = (int) quantitySpinner.getValue();
        if (quantity <= 0) {
            JOptionPane.showMessageDialog(this, "Please select a valid quantity.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double total = price * quantity;

        // ✅ Add item to the order table
        DefaultTableModel orderModel = (DefaultTableModel) orderTable.getModel();
        orderModel.addRow(new Object[]{itemId, itemName, price, quantity, total});

        // ✅ Update total price dynamically
        updateTotalPrice();

        JOptionPane.showMessageDialog(this, "Item added to order!");
    
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

