    public ManagerDashboard(Manager manager, ManagerController managerController) {
        this.manager = manager;
        this.managerController = managerController;

        setTitle("Manager Dashboard");
        setSize(600, 400);
        setDefaultCloseOperation(EXIT_ON_CLOSE);
        setLocationRelativeTo(null);

        // Set a custom background color
        JPanel panel = new JPanel();
        panel.setLayout(new GridLayout(3, 1, 10, 10)); // 10px spacing
        panel.setBackground(new Color(240, 240, 240)); // Light Gray Background
        panel.setBorder(BorderFactory.createEmptyBorder(30, 30, 30, 30)); // Add margins

        // Improved Button Styling
        JButton monitorPerformanceBtn = createStyledButton("ðŸ“Š Monitor Performance", new Color(0, 153, 255)); // Blue
        JButton resolveComplaintsBtn = createStyledButton("âš ï¸ Resolve Complaints", new Color(255, 153, 0)); // Orange
        JButton logoutBtn = createStyledButton("ðŸ”´ Logout", new Color(255, 51, 51)); // Red

        // Button Actions
        monitorPerformanceBtn.addActionListener(e -> managerController.monitorPerformance());
        resolveComplaintsBtn.addActionListener(e -> resolveComplaints());
        logoutBtn.addActionListener(e -> {
            dispose(); // Close the Manager Dashboard
            new LoginPage().setVisible(true); // Open Login Page
        });

        panel.add(monitorPerformanceBtn);
        panel.add(resolveComplaintsBtn);
        panel.add(logoutBtn);

        add(panel);
        setVisible(true);
    }

    // âœ… Custom Method for Styled Buttons
    private JButton createStyledButton(String text, Color bgColor) {
        JButton button = new JButton(text);
        button.setFont(new Font("Arial", Font.BOLD, 16));
        button.setBackground(bgColor);
        button.setForeground(Color.WHITE);
        button.setBorder(BorderFactory.createLineBorder(Color.DARK_GRAY, 2, true));
        button.setFocusPainted(false);
        return button;
    }
    
    private void resolveComplaints(){
        managerController.showReviewsAndResolveComplaint();
    }


